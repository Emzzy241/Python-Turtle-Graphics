import turtle

# Giving the app a background-color
# The python turtle module is a popular way to introduce programming concepts and create graphics

print("Drawing a Square with turtle module from python")
turtle.bgcolor("brown")
turtle.speed(1)


# Actually drawing the square shape in python
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)

turtle.done()